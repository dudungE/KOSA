package ex01;

public class MainEntry {
	
	public static void main(String[] args) {
		
		System.out.print("\nMSA th2\t 화이팅!\n\n");  // \문자 : 제어문자, \n, \t, \a, \b,...
		System.out.println(100);  // 4byte int
		System.out.println(100L);  // 8byte long   L/l
		System.out.print(12.34);  // 8byte  double
		System.out.print(12.34F);  // 4byte  float   f/F
		
		System.out.println("");
		System.out.printf("%s", "doyeon");  //sysout  Ctrl+Space bar / sout vs souf vs soutv vs soutp vs soutm
		
	}
}
